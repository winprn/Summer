const int MX=2e5+10;
const int N=1e2+10;
vector<int> b;
int a[N];
bool mark[N];
int n;

bool ok(){
    b.clear();
    for(int i=1;i<=n;++i){
        if(!mark[i])b.pb(a[i]);
    }

    bitset<MX> bit(1);
    int sum=accumulate(b.begin(),b.end(),0);
    for(auto i:b)bit|=bit<<i;

    //cout<<sum<<endl;
    return !(sum&1)&&bit[sum>>1];
}

