const int N=1e3+10;
map<pii,bool> mark;
vector<pii> adj[N];
int d[2][N],par[N];
int n,m,s,t;

void dijkstra(int id,int s){
    priority_queue<pii,vector<pii>,greater<pii>> pq;
    for(int i=1;i<=n;++i)d[id][i]=1e9;
    d[id][s]=0;
    pq.push(mp(0,s));
    while(!pq.empty()){
        int u=pq.top().se;
        pq.pop();

        for(int i=0;i<(int)adj[u].size();++i){
            pii v=adj[u][i];
            if(mark[mp(u,v.fi)])continue;
            if(d[id][v.fi]>d[id][u]+v.se){
                d[id][v.fi]=d[id][u]+v.se;
                par[v.fi]=u;
                pq.push(mp(d[id][v.fi],v.fi));
            }
        }
    }
}

