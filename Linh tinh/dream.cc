vector<string> cur;
set<string> tmp;
map<string,int> id;
int n,cnt=0;

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.precision(10);
    cout << fixed;
#ifdef LOCAL
    freopen("i", "r", stdin);
    freopen("o", "w", stdout);
#endif

    cin>>n;
    while(n--){
        char c; cin>>c;
        if(c=='E'){
            string s; cin>>s;
            cur.pb(s);
            tmp.insert(s);
            id[s]=cnt++;
        }else if(c=='D'){
            int x; cin>>x;
            while(x--&&!tmp.empty()){
                string s=cur.back();
                cur.pop_back(),tmp.erase(s);
                cnt--;
            }
        }else{
            vector<string> ev;
            int x,ans=-1; cin>>x;
            bool bad=0;
            while(x--){
                string s; cin>>s;
                if(s[0]=='!'){
                    s=s.substr(1);
                    if(tmp.find(s)!=tmp.end()){
                        ans=max(ans,(int)cur.size()-id[s]);
                    }
                }else{
                    if(tmp.find(s)!=tmp.end()){
                        ev.pb(s);
                    }else bad=1;
                }
            }

            if(!bad){
                if(ans<0)cout<<"Yes"<<endl;
                else{
                    for(string i:ev){
                        if(id[i]>=(int)cur.size()-ans){bad=1;break;}
                    }

                    if(!bad)cout<<ans<<" Just A Dream"<<endl;
                    else cout<<"Plot Error"<<endl;
                }
            }else cout<<"Plot Error"<<endl;
        }
    }

#ifdef LOCAL
    cerr << "\nTime elapsed: " << 1.0 * clock() / CLOCKS_PER_SEC << " s.\n";
#endif
}
